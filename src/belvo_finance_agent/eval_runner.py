from __future__ import annotations

import argparse
import asyncio
from pathlib import Path

from .agent import build_agent
from .logging_config import configure_logging
from .mcp_client import MCPError


QUESTIONS = [
    "What's my current balance across all my accounts?",
    "How much I spent today?",
    "when I last did a payment",
    "qual foi a data do meu ultimo gasto",
    "qual foi meu maior gasto ontem",
    "qual foi meu maior gasto em maio",
    "what was my biggest expense on May",
    "show me all my expenses in May",
    "How much did I spend on food in the last 30 days?",
    "Did my salary come in this month?",
    "Show me transactions over R$ 500 in the last 90 days.",
    "What's my biggest recurring expense?",
    "How much money came in vs went out last month?",
    "show me only PIX transactions",
    "how much did I send by PIX?",
    "how much did I receive by PIX?",
    "show me all credit card transactions",
    "what was my biggest card charge?",
    "show me all Nubank transactions",
    "list transactions specific from Nubank",
    "what was my last Nubank payment?",
    "show me Itau checking expenses",
    "show me Itau savings transactions",
    "Create a new transaction for R$ 100.",
]


async def run_eval(output_path: Path) -> None:
    agent = build_agent()
    lines = [
        "# Eval Results",
        "",
        "These spot-checks were generated by running the app's agent path against the configured MCP server.",
        "",
    ]

    for index, question in enumerate(QUESTIONS, start=1):
        lines.extend([f"## {index}. {question}", ""])
        try:
            answer = await agent.answer(question)
            lines.extend(
                [
                    answer.answer,
                    "",
                    f"- Workflow: `{answer.workflow}`",
                    f"- Tools used: {', '.join(answer.tools_used) if answer.tools_used else 'none'}",
                    f"- Filters: `{answer.filters}`",
                    f"- Debug metadata: `{answer.metadata}`",
                    f"- Caveats: {'; '.join(answer.caveats) if answer.caveats else 'none'}",
                    "- Correctness note: manually review against MCP data; the answer includes filters and evidence for verification.",
                    "",
                ]
            )
        except MCPError as exc:
            lines.extend(
                [
                    f"Could not run this question because MCP was unavailable: {exc}",
                    "",
                    "- Correctness note: not evaluated in this run.",
                    "",
                ]
            )
            break

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Run Belvo financial agent spot-check evals.")
    parser.add_argument("--output", default="evals/results.md", help="Path to write Markdown eval results.")
    args = parser.parse_args()
    configure_logging()
    asyncio.run(run_eval(Path(args.output)))
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()
